export type QuizQuestion = {
  text: string;
  options: string[];
  correctIdx: number;
};

function shuffle<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export function buildQuestions(
  pool: QAPair[],
  count = 10,
): QuizQuestion[] {
  const selected = shuffle(pool).slice(0, count);
  const allAnswers = pool.map(([, a]) => a);

  return selected.map(([text, correct]) => {
    const wrongs = shuffle(
      [...new Set(allAnswers.filter((a) => a !== correct))],
    ).slice(0, 3);
    const options = shuffle([correct, ...wrongs]);
    return { text, options, correctIdx: options.indexOf(correct) };
  });
}

export function buildMixQuestions(count = 10): QuizQuestion[] {
  const each = Math.ceil(count / 4);
  const mixed = [
    ...shuffle(CAR_QUIZ).slice(0, each),
    ...shuffle(MATH_QUIZ).slice(0, each),
    ...shuffle(PUZZLE_QUIZ).slice(0, each),
    ...shuffle(LOGO_QUIZ).slice(0, each),
  ];
  const allAnswers = [...CAR_QUIZ, ...MATH_QUIZ, ...PUZZLE_QUIZ, ...LOGO_QUIZ].map(([, a]) => a);
  const selected = shuffle(mixed).slice(0, count);

  return selected.map(([text, correct]) => {
    const wrongs = shuffle(
      [...new Set(allAnswers.filter((a) => a !== correct))],
    ).slice(0, 3);
    const options = shuffle([correct, ...wrongs]);
    return { text, options, correctIdx: options.indexOf(correct) };
  });
}

export const QUIZ_TYPES = ["carquiz", "mathquiz", "puzzle", "carlogo", "mixquiz"] as const;
export type QuizType = (typeof QUIZ_TYPES)[number];

export function getQuestions(quizType: string, count = 10): QuizQuestion[] {
  switch (quizType) {
    case "carquiz":  return buildQuestions(CAR_QUIZ, count);
    case "mathquiz": return buildQuestions(MATH_QUIZ, count);
    case "puzzle":   return buildQuestions(PUZZLE_QUIZ, count);
    case "carlogo":  return buildQuestions(LOGO_QUIZ, count);
    case "mixquiz":  return buildMixQuestions(count);
    default:         return buildQuestions(CAR_QUIZ, count);
  }
}
